public class abstract抽象类 {
    /*
    * 抽象类与接口类似，都是等待实现的，相当于在草图上给你画了个框架，等待你去实现。
    *
    * public abstract class 游戏角色初始属性{
    *
    * public abstract void 技能树--奔跑();
    *
    * }
    *
    * public class 小明 extends 游戏角色初始属性{
    *
    * public void 技能树--奔跑(){***这里是奔跑技能的实现***}
    * }
    *
    * 由此看来，抽象类通过子类继承实现，抽象方法要通过方法重写实现
    * 要注意的是，抽象的方法只能写在抽象类中等待实现，但是抽象类中可以实现普通方法
    * 抽象类不能实例化
    * 抽象类有构造器，用于子类继承
    * 抽象类的局限在于子类只能单继承一个抽象类，但是接口可以多继承
    * */
}
